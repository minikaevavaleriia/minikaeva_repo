task_2()


"""
На летней универсиаде будет проводиться по два забега. Чтобы соревнования были честными,
участники должны распределяться между забегами случайным образом.
Напишите программу, печатающую случайные номера забегов (1 или 2) до тех пор, пока не будет введено «off».
После генерации каждого номера должно печататься:
1) «Ваш номер: _».
2) «Участников в первом забеге: _», «Участников во втором забеге: _».
"""


def task3():
    team1 = 0
    team2 = 0
    while True:
        user = input('Press enter')
        if user != 'off':
            number = randint(1, 2)
            print('Your number is:', number)
            if number == 1:
                team1 += 1
            else:
                team2 += 1
            print('Участников в первом забеге:', team1)
            print('Участников во втором забеге:', team2)
        else:
            break


task3()


"""
Во время хоккейных матчей на игроков может накладываться дисциплинарный штраф — удаление с поля на 2 или 10 минут.
Программа должна:
1) Спрашивать «Удалить с поля?» и запрашивать количество минут штрафа.
2) Печатать сообщение: «Вы удалена с поля на _ минут(-ы)» и ставить паузу в работе на это время.
3) Спустя 2 или 10 минут печатать новое сообщение: «Возвращайтесь на поле!»
Дабы не ждать 2 и 10 минут сделайте 2 и 10 секунд.
"""


def task_4():
    while True:
        delete = int(input('Удалить с поля? 1. Yes 2. No'))
        if delete == 1:
            time_input = int(input('На сколько минут?'))
            print('Вы удалена с поля на', time_input, 'минут(-ы)')
            sleep(time_input)
            print('Возвращайтесь на поле!')
        else:
            pass


task_4()


