from random import *
from time import *


"""
В быстрых шахматах на принятие решений для всех ходов игроку даётся 30 минут. Программа должна:
Предлагать ввод хода (например, E2–E4) и считать потраченное время.
После получения хода печатать оставшееся время в минутах.
Если 30 минут закончились или игрок вводит «off» — завершать работу.
Оформить в виде функции.
"""


def task_1():
    t = monotonic()
    start = round(time(), 0)
    while monotonic() - t != 0:
        user_input = input('Enter: ')
        if user_input != 'off':
            check_time = round(time(), 0)
            left = (1800 - (check_time - start)) / 60
            print('Осталось:', round(left, 2), 'мин')
        else:
            break


task_1()


"""
В каждом заплыве участвуют два случайных спортсмена из разных сборных. Напиши программу для печати номеров спортсменов.
1) Программа должна запрашивать количество спортсменов в каждой сборной с сообщением: «Число участников сборной _:».
2) Затем должна печататься пара случайных спортсменов из разных сборных для заплыва в формате: «Пловец _ - пловец _».
"""


def task_2():
    team1 = int(input('Число участников сборной 1:'))
    team2 = int(input('Число участников сборной 2:'))
